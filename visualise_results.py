    min_label_gap = 0.05
    adjusted_ys = list(right_label_ys)
    for i in range(len(adjusted_ys) - 1, 0, -1):
        if adjusted_ys[i] - adjusted_ys[i - 1] < min_label_gap:
            adjusted_ys[i - 1] = adjusted_ys[i] - min_label_gap

