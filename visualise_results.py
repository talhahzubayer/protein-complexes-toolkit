    ax.text(0.50, 1.06, "Classified Variant Flow: Clinical Significance -> Structural Context",
            ha='center', va='bottom', fontsize=14, fontweight='bold',
            transform=ax.transAxes)
    # Footer annotation (merged to avoid overlap)
